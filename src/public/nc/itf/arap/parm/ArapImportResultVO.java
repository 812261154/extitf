package nc.itf.arap.parm;

public class ArapImportResultVO {
	private String pk_importresult;
	private String fromsys;
	private String number;
	private String completed;
	private String message;
	private String ts;
	
	
	public String getTs() {
		return ts;
	}
	public void setTs(String ts) {
		this.ts = ts;
	}
	public String getPk_importresult() {
		return pk_importresult;
	}
	public void setPk_importresult(String pk_importresult) {
		this.pk_importresult = pk_importresult;
	}
	public String getFromsys() {
		return fromsys;
	}
	public void setFromsys(String fromsys) {
		this.fromsys = fromsys;
	}
	public String getNumber() {
		return number;
	}
	public void setNumber(String number) {
		this.number = number;
	}
	public String getCompleted() {
		return completed;
	}
	public void setCompleted(String completed) {
		this.completed = completed;
	}
	public String getMessage() {
		return message;
	}
	public void setMessage(String message) {
		this.message = message;
	}
	
	
}
